public class Student {
    int time;
    String email;
    String name;
    int first;
    int second;
    int third;
    int fourth;
    int fifth;

    public Student(double time, String email, String name, int first, int second, int third, int fourth, int fifth) {
        this.time = time; 
        this.email = email;
        this.name = name;
        this.first = first;
        this.second = second;
        this.third = third;
        this.fourth = fourth;
        this.fifth = fifth;
    }
}



